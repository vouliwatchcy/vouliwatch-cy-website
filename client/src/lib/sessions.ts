export function getAllSessions(): Session[] {
  return Object.values(mockSessions).sort(
    (a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()
  );
}

export function getSessionByDate(date: string): Session | undefined {
  return mockSessions[date];
}

export function getLatestSession(): Session | undefined {
  const sessions = getAllSessions();
  return sessions[0];
}

export function getSessionMetadata(session: Session): SessionMetadata {
  const statusCounts = {
    approved: 0,
    postponed: 0,
    rejected: 0,
    withdrawn: 0,
  };

  session.topics.forEach((topic) => {
    switch (topic.status) {
      case 'Εγκρίθηκε':
        statusCounts.approved++;
        break;
      case 'Αναβλήθηκε':
        statusCounts.postponed++;
        break;
      case 'Απορρίφθηκε':
        statusCounts.rejected++;
        break;
      case 'Αποσύρθηκε':
        statusCounts.withdrawn++;
        break;
    }
  });

  return {
    date: session.date,
    title: session.title,
    topicCount: session.topics.length,
    approved: statusCounts.approved,
    postponed: statusCounts.postponed,
    rejected: statusCounts.rejected,
    withdrawn: statusCounts.withdrawn,
  };
}

export function getStatusColor(status: SessionStatus): string {
  switch (status) {
    case 'Εγκρίθηκε':
      return '#16a34a'; // green
    case 'Αναβλήθηκε':
      return '#d97706'; // amber
    case 'Απορρίφθηκε':
      return '#dc2626'; // red
    case 'Αποσύρθηκε':
      return '#6b7280'; // gray
    case 'Δεν επιβεβαιώθηκε':
      return '#475569'; // slate
    default:
      return '#6b7280';
  }
}
